package section6;

public class Switchcase {

	public static void main(String[] args) {
		String days="sunday";
		 switch(days) {
		 
		 case "sunday"->
			 System.out.println("it is sunday");
			 
		 case "monday"->System.out.println("this is a monday");
			 
		 
		 
		 
	 default->System.out.println("it is not a day");
	}

	}

}
